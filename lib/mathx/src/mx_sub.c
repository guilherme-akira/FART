#include "mathx.h"

t_tuple	mx_sub(t_tuple a, t_tuple b)
{
	return (mx_tuple(a.pos[0] - b.pos[0], \
					a.pos[1] - b.pos[1], \
					a.pos[2] - b.pos[2], \
					a.pos[3] - b.pos[3]));
}
