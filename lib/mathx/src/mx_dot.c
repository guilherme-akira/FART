#include "mathx.h"

double	mx_dot(t_tuple a, t_tuple b)
{
	return (a.pos[0] * b.pos[0] + \
			a.pos[1] * b.pos[1] + \
			a.pos[2] * b.pos[2] + \
			a.pos[3] * b.pos[3]);
}
