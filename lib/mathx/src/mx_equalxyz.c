#include "mathx.h"

bool	mx_equalxyz(t_tuple a, t_tuple b)
{
	return (mx_equal(a.pos[0], b.pos[0]) && \
			mx_equal(a.pos[1], b.pos[1]) && \
			mx_equal(a.pos[2], b.pos[2]));
}
